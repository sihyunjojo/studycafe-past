package basic.studyCafe.vo;

public class BoardVO {

	private int board_number;
	private String user_id;
	private int board_pagenumber;
	private String board_title;
	private String board_kind;
	private String board_time;
	private int board_readcount;
	private String board_content;
	private String board_filename;
	private String board_popup;
	
	public int getBoard_number() {
		return board_number;
	}
	public void setBoard_number(int board_number) {
		this.board_number = board_number;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getBoard_pagenumber() {
		return board_pagenumber;
	}
	public void setBoard_pagenumber(int board_pagenumber) {
		this.board_pagenumber = board_pagenumber;
	}
	public String getBoard_title() {
		return board_title;
	}
	public void setBoard_title(String board_title) {
		this.board_title = board_title;
	}
	public String getBoard_kind() {
		return board_kind;
	}
	public void setBoard_kind(String board_kind) {
		this.board_kind = board_kind;
	}
	public String getBoard_time() {
		return board_time;
	}
	public void setBoard_time(String board_time) {
		this.board_time = board_time;
	}
	public int getBoard_readcount() {
		return board_readcount;
	}
	public void setBoard_readcount(int board_readcount) {
		this.board_readcount = board_readcount;
	}
	public String getBoard_content() {
		return board_content;
	}
	public void setBoard_content(String board_content) {
		this.board_content = board_content;
	}
	public String getBoard_filename() {
		return board_filename;
	}
	public void setBoard_filename(String board_filename) {
		this.board_filename = board_filename;
	}
	public String getBoard_popup() {
		return board_popup;
	}
	public void setBoard_popup(String board_popup) {
		this.board_popup = board_popup;
	}
	
	

}
