package basic.studyCafe.vo;

public class CartProductVO {
	private String user_id;
	private int prod_number;
	private char prod_cartstate;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public int getProd_number() {
		return prod_number;
	}
	public void setProd_number(int prod_number) {
		this.prod_number = prod_number;
	}
	public char getProd_cartstate() {
		return prod_cartstate;
	}
	public void setProd_cartstate(char prod_cartstate) {
		this.prod_cartstate = prod_cartstate;
	}
	
	
	
	
}
